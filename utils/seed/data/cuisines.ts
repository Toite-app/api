// All cuisine types except general
export const SPECIFIC_CUISINES: CuisineType[] = [
  "italian",
  "asian",
  "american",
  "french",
  "mexican",
  "mediterranean",
];

export const ALL_CUISINES: CuisineType[] = [...SPECIFIC_CUISINES, "general"];

// Helper to get all dishes from all specific cuisines (for "general" restaurants)
export function getAllDishes(): DishData[] {
  return SPECIFIC_CUISINES.flatMap((cuisine) => CUISINES[cuisine].dishes);
}

// Helper to get dishes for a specific cuisine (or all if general)
export function getDishesForCuisine(cuisineType: CuisineType): DishData[] {
  if (cuisineType === "general") {
    return getAllDishes();
  }
  return CUISINES[cuisineType].dishes;
}

// Helper to get menu categories for a cuisine
export function getMenuCategoriesForCuisine(
  cuisineType: CuisineType,
): string[] {
  if (cuisineType === "general") {
    return CUISINES.general.menuCategories;
  }
  return CUISINES[cuisineType].menuCategories;
}
